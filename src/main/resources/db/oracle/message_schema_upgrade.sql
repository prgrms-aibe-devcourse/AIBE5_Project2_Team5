DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_TABLES
     WHERE TABLE_NAME = 'CHAT_MESSAGE_REACTIONS';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE q'[
            CREATE TABLE CHAT_MESSAGE_REACTIONS (
                REACTION_ID NUMBER(19) PRIMARY KEY,
                MESSAGE_ID NUMBER(19) NOT NULL,
                USER_ID NUMBER(19) NOT NULL,
                EMOJI VARCHAR2(50) NOT NULL,
                CREATED_MESSAGE_REACTION TIMESTAMP NOT NULL,
                CONSTRAINT UK_MSG_REACT UNIQUE (MESSAGE_ID, USER_ID, EMOJI),
                CONSTRAINT FK_MSG_REACT_MSG FOREIGN KEY (MESSAGE_ID) REFERENCES CHAT_MESSAGES (MESSAGE_ID) ON DELETE CASCADE,
                CONSTRAINT FK_MSG_REACT_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID)
            )
        ]';
    END IF;
END;
