                TITLE VARCHAR2(200) NOT NULL,
                STATUS VARCHAR2(20) NOT NULL,
                SORT_ORDER NUMBER(10) NOT NULL,
                DESIGNER_CONFIRMED NUMBER(1) DEFAULT 0 NOT NULL,
                CLIENT_CONFIRMED NUMBER(1) DEFAULT 0 NOT NULL,
                CREATED_MESSAGE_PROCESS TIMESTAMP NOT NULL,
                CONSTRAINT FK_MSG_PROC_CONV FOREIGN KEY (CONVERSATION_ID) REFERENCES MESSAGE_CONVERSATIONS (CONVERSATION_ID) ON DELETE CASCADE,
                CONSTRAINT CK_MSG_PROC_STATUS CHECK (STATUS IN ('pending', 'in-progress', 'completed')),
                CONSTRAINT CK_MSG_PROC_DES CHECK (DESIGNER_CONFIRMED IN (0, 1)),
                CONSTRAINT CK_MSG_PROC_CLI CHECK (CLIENT_CONFIRMED IN (0, 1))
            )
        ]';
    END IF;
